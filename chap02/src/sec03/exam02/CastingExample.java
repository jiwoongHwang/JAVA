package sec03.exam02;

public class CastingExample {

	public static void main(String[] args) {
		int a = 10;
		byte b = (byte)a;
		System.out.println(b);
		
		int IntValue = 44032;
		char charValue = (char) IntValue;
		System.out.println(charValue);
		
		double x = 3.14;
		int y = (int) x;
		System.out.println(y);
	}

}
