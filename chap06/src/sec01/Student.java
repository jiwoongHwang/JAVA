package sec01;

public class Student {
		int age;	//나이
		String gender;	//성별
		String name;	//이름
		
		public Student() {	//Student의 생성자, 객체 생성시 초기화
		}
			
		public int getAge() {
			return age;
		}
		
		public String getGender() {
			return gender;
		}



		public void setGender(String gender) {
			this.gender = gender;
		}



		public String getName() {
			return name;
		}



		public void setName(String name) {
			this.name = name;
		}






		public int setAge(int age) {	//메소드
			return age;
		}

}
